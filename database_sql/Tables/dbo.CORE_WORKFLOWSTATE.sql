CREATE TRIGGER [dbo].[TR_CORE_WORKFLOWSTATE_TRANSLATION]
		   ON  [dbo].[CORE_WORKFLOWSTATE]
		   AFTER INSERT , UPDATE
		AS 
		BEGIN
			SET NOCOUNT ON;

			IF TRIGGER_NESTLEVEL() <= 1
			BEGIN

				--VOOR ELKE ROW DIE INGEVOERD IS, DE SP CORE_TRANSLATION_FILLFIELDS AANROEPEN
				DECLARE @TABLENAME NVARCHAR(150) 

				SELECT @TABLENAME = OBJECT_NAME(parent_object_id) 
							FROM sys.objects 
							WHERE sys.objects.name = OBJECT_NAME(@@PROCID)
			
				EXEC CORE_TRANSLATION_CHECKFULLTABLE @TABLENAME
		
			END
		END
GO
SET QUOTED_IDENTIFIER ON
GO
SET ANSI_NULLS ON
GO
CREATE TRIGGER [dbo].[TR_CORE_WORKFLOWSTATE_TS_LASTMODIFIED]
		   ON  [dbo].[CORE_WORKFLOWSTATE]
		   AFTER UPDATE
		AS 
		BEGIN
			SET NOCOUNT ON;

			UPDATE T SET 
				T.TS_LASTMODIFIED = GETDATE()
			FROM 
				CORE_WORKFLOWSTATE  T
			INNER JOIN INSERTED I ON T.ID = I.ID
		END
GO
