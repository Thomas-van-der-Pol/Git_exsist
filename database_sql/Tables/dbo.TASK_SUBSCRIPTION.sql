				CREATE TRIGGER [dbo].[TR_TASK_SUBSCRIPTION_TS_LASTMODIFIED]
						   ON  [dbo].[TASK_SUBSCRIPTION]
						   AFTER UPDATE
						AS 
						BEGIN
							SET NOCOUNT ON;

							UPDATE T SET 
								T.TS_LASTMODIFIED = GETDATE()
							FROM 
								TASK_SUBSCRIPTION T
							INNER JOIN INSERTED I ON T.ID = I.ID
						END
GO
