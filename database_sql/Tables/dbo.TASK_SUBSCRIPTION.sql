[TS_LASTMODIFIED] [datetime] NULL CONSTRAINT [DF_TASK_SUBSCRIPTION_TS_LASTMODIFIED] DEFAULT (getdate()),
[ACTIVE] [bit] NOT NULL CONSTRAINT [DF_TASK_SUBSCRIPTION_ACTIVE] DEFAULT ((1)),
[FK_CORE_USER] [int] NULL,
[FK_TASK] [int] NULL
) ON [PRIMARY]
GO
